#include  <stdio.h>

int main()
{
    // 如果变量是int, 计算产生的小数, 会被%d截断, 只保留整数部分

    printf("7/12=%d, 10/3=%d\n", 7/12, 10/3);

    // 小心计算 陷阱
    printf("10/3*3=%d\n", 10/3*3);      // 答案是 9 不是10

    // 注意: 如果格式化 %f 则计算时必须使用浮点数字, 否则结果是错误的
    printf("10/3*3=%.2f\n", 10/3.0*3);      // 10.00

    // 如果是定义变量为 double, 再参与计算,就会得到正确结果
    double a,b;
    // 接收浮点数,必须使用%lf
    scanf("%lf %lf", &a, &b);

    printf("a/b=%.2f\n", a/b);
    return 0;
}